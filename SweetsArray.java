public class SweetsArray {
    public static void main(String[] args) {
        
        String[] sweets = {
            "Chocolate",
            "Candy",
            "Lollipop",
            "Marshmallow",
            "Jellybean",
            "Gummy Bear",
            "Fudge",
            "Brownie",
            "Cookie",
            "Cupcake"
        };

        
        for (String sweet : sweets) {
            System.out.println(sweet);
        }
    }
}
